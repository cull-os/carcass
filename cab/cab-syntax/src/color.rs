const fn style(r: u8, g: u8, b: u8) -> yansi::Style {
    let color = if (0.2126 * r as f32 + 0.7152 * g as f32 + 0.0722 * b as f32) < 140.0 {
        yansi::Color::Rgb(0xFF, 0xFF, 0xFF)
    } else {
        yansi::Color::Rgb(0, 0, 0)
    };

    color.on_rgb(r, g, b)
}

